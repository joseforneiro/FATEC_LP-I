
public class Capital {
	String sigla;
	String capital;
	
	//Construtor
	public Capital() {
		
	}

	//Metodos de acesso dos tipos Getter e Setter
	public String getSigla() {
		return sigla;
	}

	public void setSigla(String sigla) {
		this.sigla = sigla;
	}

	public String getCapital() {
		return capital;
	}

	public void setCapital(String capital) {
		this.capital = capital;
	}
	
}
